// Strings that have a zero will result in multiplication of zero. So splitting by them is okay.
var nonZeroStrings = inputNumber.split("0");
var nonZeroStringsLongerThan13Digits = nonZeroStrings.filter(t => t.length >= 13);
// console.log(nonZeroStringsLongerThan13Digits);

// Need to get all possible 13 digit strings.
function take13Digits (number) {
  return /\d{13}/g.exec(number)[0];
}

var allPossibleNumbers = [];
nonZeroStringsLongerThan13Digits.forEach(t => {
  var digits = t.split("");
  while(digits.length >= 13) {
    allPossibleNumbers.push(take13Digits(digits.join("")));
    digits.shift();
  }
});

console.log(allPossibleNumbers);


// Get the answer.
function getMultiplication(number) {
  var digits = number.split("");
  var mult = 1;
  digits.forEach(t => mult *= t);
  return mult;
}

var largestMultiplication = 0;
var numberWithLargestMultiplication = 0;
allPossibleNumbers.forEach(t => {
  var mult = getMultiplication(t);
  if (mult > largestMultiplication) {
    largestMultiplication = mult;
    numberWithLargestMultiplication = t;
  }
});

console.log("Winning number:", numberWithLargestMultiplication);
